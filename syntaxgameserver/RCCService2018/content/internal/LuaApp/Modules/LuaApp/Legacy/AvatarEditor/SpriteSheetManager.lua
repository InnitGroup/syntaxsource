-- api
local guiService = game:GetService'GuiService'

local function getDescendants(obj, t)
	t = t or {}
	for _, v in next, obj:GetChildren() do
		table.insert(t, v)
		getDescendants(v, t)
	end
	return t
end

local this = {}
this.enabled = true
this.all = function()
	return sprites
end
this.info = function(name)
	return sprites[name]
end
this.getScale = function()
	return SIMULATE_RESOLUTION_SCALE or guiService:GetResolutionScale()
end
this.equipDescendants = function(gui, name)
	for _, obj in next, getDescendants(gui) do
		if obj.Name == 'SpriteName'
			and obj.ClassName == 'StringValue'
			and obj.Parent
			and not obj.Parent:FindFirstChild'DontMagicallyAssignToSpriteSheet' then
			this.equip(obj.Parent, obj.Value)

			if obj.Parent:FindFirstChild'SpriteIsReversed' then
				obj.Parent.ImageRectOffset = obj.Parent.ImageRectOffset + Vector2.new(obj.Parent.ImageRectSize.x, 0)
				obj.Parent.ImageRectSize = obj.Parent.ImageRectSize * Vector2.new(-1, 1)
			end
		end
	end
end
this.equip = function(gui, name, doNotMutateSprite)
	if not doNotMutateSprite and not gui:FindFirstChild'DoNotMutateSprite' then
		pcall(function()
			local tryMutation = function(n)
				local scaleName = name..'@'..n..'x'
				if sprites[scaleName] then
					if gui.SliceCenter ~= Rect.new() then
						gui.SliceCenter = Rect.new(
							gui.SliceCenter.Min.x*n, gui.SliceCenter.Min.y*n,
							gui.SliceCenter.Max.x*n, gui.SliceCenter.Max.y*n
						)
					end

					return scaleName
				end
			end

			local resScale = this.getScale()
			if resScale > 2 then
				name = tryMutation(3) or tryMutation(2) or name
			elseif resScale > 1 then
				name = tryMutation(2) or name
			end
		end)
	end

	if sprites[name] then
		local content = sprites[name].imageId

		gui.ImageRectSize = sprites[name].imageRectSize
		gui.ImageRectOffset = sprites[name].imageRectOffset
		gui.Image = content
	else
		warn('BAD SPRITE NAME: "'..tostring(name)..'".  Tried to equip to '..gui:GetFullName())
		gui.ImageRectSize = Vector2.new(0, 0)
		gui.ImageRectOffset = Vector2.new(0, 0)
		gui.Image = 'rbxasset://textures/ui/ErrorIcon.png'
	end
end

return this